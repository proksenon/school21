#include "libft.h"

void	ft_putnbr(int n)
{
	int sig;

	sig = 0;
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			sig = 1;
			ft_putchar('-');
		}
		n = (sig ? -n : n);
		if (n > 9)
			ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
