#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)src;
	while (str[i] != '\0')
	{
		dst[i] = str[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
