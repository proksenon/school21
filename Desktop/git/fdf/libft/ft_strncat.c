#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*str;
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	str = (char *)s2;
	while (s1[i] != '\0')
		i++;
	while ((str[j] != '\0') && (j < n))
	{
		s1[i] = str[j];
		j++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
