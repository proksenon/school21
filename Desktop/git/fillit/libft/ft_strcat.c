#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*str;
	int		i;
	int		j;

	i = 0;
	j = 0;
	str = (char *)s2;
	while (s1[i] != '\0')
		i++;
	while (str[j] != '\0')
	{
		s1[i] = str[j];
		j++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
